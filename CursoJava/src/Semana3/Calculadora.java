package Semana3;

public class Calculadora {

	
	
	public static double sumar(double unNombre, double otroNumero) {
		
		return unNombre + otroNumero;
		
	}
	public static double restar(double unNombre, double otroNumero) {
		
		return unNombre - otroNumero;
		
	}
	public static double multiplicar(double unNombre, double otroNumero) {
		
		return unNombre * otroNumero;
		
	}
	public static double dividir(double unNombre, double otroNumero) {
		
		return unNombre / otroNumero;
		
	}
	
}
